import json

def update(f, d):
    with open(f, 'r', encoding='utf-8') as file: 
        data = json.load(file)
    data['meetings'] = d
    with open(f, 'w', encoding='utf-8') as file: 
        json.dump(data, file, ensure_ascii=False, indent=2)

